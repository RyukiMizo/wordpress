	public function hex_2_bin( $hex ) {
		if( function_exists( 'hex2' . 'bin' ) === false ) {
			$thk_pack = 'pa' . 'ck';
			return preg_match( '/^[0-9a-f]+$/i', $hex ) ? $thk_pack( 'H*', (string)$hex ) : $hex;
